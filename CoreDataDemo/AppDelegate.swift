import UIKit
import CoreData

@UIApplicationMain
class AppDelegate: UIResponder, UIApplicationDelegate, UISplitViewControllerDelegate {

    var window: UIWindow?
    var coordinator: AppCoordinator!


    func application(_ application: UIApplication, didFinishLaunchingWithOptions launchOptions: [UIApplication.LaunchOptionsKey: Any]?) -> Bool {
        // Override point for customization after application launch.

        if let rootVC = window?.rootViewController as? UINavigationController, let tableVC = rootVC.topViewController as? TeamTableViewController {
            coordinator = AppCoordinator(rootViewController: tableVC)
            tableVC.coordinator = coordinator
            Team.insertSampleData(context: coordinator.coreDataStack.viewContext)
        }
        return true
    }

